    private void txt_radiusKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_radiusKeyPressed
        // TODO add your handling code here:
        if ( Radius.isSelected()==true && (evt.getKeyChar() >= '0' && evt.getKeyChar() <= '9' || (evt.getKeyChar() == 8) || (evt.getKeyChar() == 10) || evt.getKeyChar()== '.'))  {
            int l=txt_radius.getText().length();//System.out.println(len);
        if((l<=6) || (evt.getKeyChar()==8)){
            String value = txt_radius.getText();
            if((evt.getKeyChar() == '.') && value.contains(".")){
                txt_radius.setEditable(false);
            }
            else{
                txt_radius.setEditable(true);
                if((evt.getKeyChar() == 10)){
                    if("".equals(txt_radius.getText())){
                        JOptionPane.showMessageDialog(null,"PLEASE ENTER THE RADIUS"); 
                        txt_radius.requestFocus();txt_radius.setFocusable(true);
                    }
                    else{
                        calculate.requestFocus();calculate.setFocusable(true);
                    }
                }
            }
        }
        else{
            txt_radius.setEditable(false);
        }
            
        }
        else {
            txt_radius.setEditable(false);
        }
    }//GEN-LAST:event_txt_radiusKeyPressed

