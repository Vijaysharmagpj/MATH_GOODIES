    private void calculateFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_calculateFocusGained
        // TODO add your handling code here:
        float area=0,perimeter=0;
        String i=null;
        if ( Diameter.isSelected()==true){
            if("".equals(txt_diameter.getText())){
                txt_diameter.requestFocus();txt_diameter.setFocusable(true);
                JOptionPane.showMessageDialog(null,"PLEASE ENTER THE DIAMETER");
            }
            else{
                i="diameter";
                float input_diameter=Float.parseFloat(txt_diameter.getText());
                CircleSolver obj=new CircleSolver();
                perimeter=obj.computeParimeter (input_diameter,i);
                area=obj.computeArea (input_diameter,i);
                txt_Perimeter.setText(String.valueOf(perimeter));
                txt_Area.setText(String.valueOf(area));
            }
        }

        if ( Radius.isSelected()==true){
            if("".equals(txt_radius.getText())){
                txt_radius.requestFocus();txt_radius.setFocusable(true);
                JOptionPane.showMessageDialog(null,"PLEASE ENTER THE RADIUS");
            }
            else{
                i="radius";
                float input_diameter=Float.parseFloat(txt_radius.getText());
                CircleSolver obj=new CircleSolver();
                perimeter=obj.computeParimeter (input_diameter,i);
                area=obj.computeArea (input_diameter,i);
                txt_Perimeter.setText(String.valueOf(perimeter));
                txt_Area.setText(String.valueOf(area));
            }
        }
    }//GEN-LAST:event_calculateFocusGained

