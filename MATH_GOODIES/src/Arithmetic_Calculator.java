    private void btn_equlFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_btn_equlFocusGained
        // TODO add your handling code here:
        if("".equals(query_display.getText())  || "Enter an Expression : ".equals(query_display.getText())){
            JOptionPane.showMessageDialog(null,"PLEASE ENTER AN EXPRESSION");
            btn_equl.setFocusable(false);query_display.requestFocus();query_display.setFocusable(true);
        }
        else{
            String s1=query_display.getText();
            ArithmeticCalculator obj=new ArithmeticCalculator(s1);
            boolean flag=obj.validate();
            if(flag==false){
                btn_sub.setFocusable(true); btn_sub.requestFocus(true);
                JOptionPane.showMessageDialog(null,"SYNTAX ERROR");
            }
            else{
                result=(float) obj.computeExpression();
                answer_display.setText("");
                answer_display.setText("Ans="+result);
            } 
        }
               
    }//GEN-LAST:event_btn_equlFocusGained

